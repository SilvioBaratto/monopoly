"""Tests for docs/video_script.md — written BEFORE the document (TDD).

Acceptance criteria (GitHub Issue #52):
  (a) File docs/video_script.md exists with all 10 required sections.
  (b) Each section uses H2 headers, blockquotes for narration, relative figure paths.
  (c) A summary table at the top maps section → figure → key statistic.
  (d) Each section includes a timing estimate.
  (e) Total word count is between 2000 and 4000.
  (f) All referenced figure filenames match those generated by the narrative script.
  (g) Key statistics are cited (Jail, Illinois, orange/arancione, 3 case).
  (h) Tone is educational and accessible (written in Italian).
"""

from __future__ import annotations

import re
from pathlib import Path

import pytest

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------

_DOCS_DIR = Path(__file__).parent.parent / "docs"
_SCRIPT_PATH = _DOCS_DIR / "video_script.md"

# Figure filenames generated by come_vincere_al_monopoli.py
_EXPECTED_FIGURES = {
    "heatmap.png",
    "roi_bars.png",
    "win_rate_curves.png",
    "net_worth.png",
    "game_animation.mp4",
}

# Required H2 section titles (partial matches in Italian)
_EXPECTED_SECTION_COUNT = 10

# Key statistics that must appear somewhere in the document
_KEY_STATS = [
    "5.89",  # Jail landing probability
    "3.18",  # Illinois Ave probability
    "16.7",  # 7 is the most frequent dice sum
]

# Key Italian terms indicating educational tone
_ITALIAN_KEYWORDS = [
    "probabilità",
    "casella",
    "proprietà",
    "strategia",
    "Prigione",
]


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------


@pytest.fixture(scope="module")
def script_content() -> str:
    """Return the full text of docs/video_script.md."""
    return _SCRIPT_PATH.read_text(encoding="utf-8")


@pytest.fixture(scope="module")
def h2_sections(script_content: str) -> list[str]:
    """Return all H2 section titles found in the document."""
    return re.findall(r"^## .+", script_content, re.MULTILINE)


# ---------------------------------------------------------------------------
# (a) File existence
# ---------------------------------------------------------------------------


class TestFileExists:
    """The document must exist at the expected path."""

    def test_docs_directory_exists(self) -> None:
        """docs/ directory must exist."""
        assert _DOCS_DIR.exists(), f"docs/ directory not found at {_DOCS_DIR}"
        assert _DOCS_DIR.is_dir()

    def test_video_script_file_exists(self) -> None:
        """docs/video_script.md must exist."""
        assert _SCRIPT_PATH.exists(), f"File not found: {_SCRIPT_PATH}"
        assert _SCRIPT_PATH.is_file()

    def test_file_is_not_empty(self, script_content: str) -> None:
        """docs/video_script.md must not be empty."""
        assert len(script_content.strip()) > 0


# ---------------------------------------------------------------------------
# (a) Section count and structure
# ---------------------------------------------------------------------------


class TestSectionStructure:
    """The document must have exactly 10 H2 sections."""

    def test_has_ten_h2_sections(self, h2_sections: list[str]) -> None:
        """Document must contain exactly 10 H2 section headers."""
        assert len(h2_sections) == _EXPECTED_SECTION_COUNT, (
            f"Expected {_EXPECTED_SECTION_COUNT} H2 sections, "
            f"found {len(h2_sections)}: {h2_sections}"
        )

    def test_sections_are_numbered(self, h2_sections: list[str]) -> None:
        """Each H2 section must include a section number."""
        for section in h2_sections:
            has_number = bool(re.search(r"\d+", section))
            assert has_number, f"Section has no number: {section!r}"

    def test_has_h1_title(self, script_content: str) -> None:
        """Document must have an H1 title."""
        h1_titles = re.findall(r"^# .+", script_content, re.MULTILINE)
        assert len(h1_titles) >= 1, "Document must have at least one H1 title"


# ---------------------------------------------------------------------------
# (b) Narration blockquotes
# ---------------------------------------------------------------------------


class TestNarrationBlockquotes:
    """Each section must contain at least one blockquote for narration text."""

    def test_document_contains_blockquotes(self, script_content: str) -> None:
        """Document must contain blockquote syntax (lines starting with >)."""
        blockquote_lines = [
            line for line in script_content.splitlines() if line.startswith(">")
        ]
        assert len(blockquote_lines) >= 10, (
            f"Expected at least 10 blockquote lines, found {len(blockquote_lines)}"
        )

    def test_blockquotes_appear_in_most_sections(self, script_content: str) -> None:
        """At least 8 of the 10 sections must contain blockquote narration."""
        # Split by H2 and count sections with blockquotes
        sections = re.split(r"^## ", script_content, flags=re.MULTILINE)
        sections_with_blockquotes = sum(1 for s in sections if ">" in s)
        assert sections_with_blockquotes >= 8, (
            f"Expected ≥8 sections with blockquotes, found {sections_with_blockquotes}"
        )


