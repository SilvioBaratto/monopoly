class TestItalianTone:
    """Document must be written in Italian with educational tone."""

    @pytest.mark.parametrize("keyword", _ITALIAN_KEYWORDS)
    def test_italian_keyword_present(self, script_content: str, keyword: str) -> None:
        """Key Italian words must appear in the document."""
        assert keyword in script_content, (
            f"Italian keyword '{keyword}' not found — document may not be in Italian"
        )

    def test_no_english_section_headers(self, h2_sections: list[str]) -> None:
        """Section headers must be in Italian (no English-only section titles)."""
        english_only_words = {"introduction", "conclusion", "section", "chapter"}
        for section in h2_sections:
            section_lower = section.lower()
            is_english_only = all(word in section_lower for word in english_only_words)
            assert not is_english_only, (
                f"Section header appears to be in English: {section!r}"
            )
