# (b) Relative figure paths
# ---------------------------------------------------------------------------


class TestFigurePaths:
    """All referenced figures must use relative paths and match script outputs."""

    def test_figures_use_relative_paths(self, script_content: str) -> None:
        """Figure references must use '../figures/' relative path prefix."""
        figure_refs = re.findall(r"\.\./figures/[\w.]+", script_content)
        assert len(figure_refs) >= len(_EXPECTED_FIGURES) - 1, (
            f"Expected at least {len(_EXPECTED_FIGURES) - 1} figure references "
            f"with '../figures/' prefix, found {len(figure_refs)}"
        )

    @pytest.mark.parametrize("figure_name", sorted(_EXPECTED_FIGURES))
    def test_each_expected_figure_is_referenced(
        self, script_content: str, figure_name: str
    ) -> None:
        """Each figure generated by the narrative script must be referenced."""
        assert figure_name in script_content, (
            f"Figure '{figure_name}' not referenced in the video script"
        )


# ---------------------------------------------------------------------------
# (c) Summary table
# ---------------------------------------------------------------------------


class TestSummaryTable:
    """A summary table must appear near the top of the document."""

    def test_document_contains_table(self, script_content: str) -> None:
        """Document must contain a Markdown table (lines with | separators)."""
        table_lines = [
            line
            for line in script_content.splitlines()
            if line.strip().startswith("|") and "|" in line[1:]
        ]
        assert len(table_lines) >= 5, (
            f"Expected ≥5 table rows, found {len(table_lines)}"
        )

    def test_table_references_figures(self, script_content: str) -> None:
        """Summary table must reference figure filenames."""
        # Find the table section (near the top, before first H2)
        pre_sections = script_content.split("##")[0]
        has_figure_ref = any(fig in pre_sections for fig in _EXPECTED_FIGURES)
        assert has_figure_ref, (
            "Summary table at the top must reference figure filenames"
        )


# ---------------------------------------------------------------------------
# (d) Timing estimates
# ---------------------------------------------------------------------------


class TestTimingEstimates:
    """Each section must include a timing estimate."""

    def test_document_contains_timing_estimates(self, script_content: str) -> None:
        """Document must contain timing estimates in seconds or minutes."""
        timing_pattern = re.compile(r"~?\d+\s*(second[io]|minut[io])", re.IGNORECASE)
        timings = timing_pattern.findall(script_content)
        assert len(timings) >= 8, (
            f"Expected ≥8 timing estimates, found {len(timings)}: {timings}"
        )

    def test_timing_estimates_are_reasonable(self, script_content: str) -> None:
        """Timing values must be plausible (between 30s and 300s per section)."""
        second_values = [
            int(m)
            for m in re.findall(r"~?(\d+)\s*second[io]", script_content, re.IGNORECASE)
        ]
        for val in second_values:
            assert 30 <= val <= 300, (
                f"Timing estimate {val}s is outside plausible range (30–300s)"
            )


